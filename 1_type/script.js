// 数値(number)
// 整数も小数もすべて数値
// --python-- int, float
// --pyhton-- print(123)
const variableNumber = 2;
console.log(typeof variableNumber);
console.log(4.56);
console.log(typeof 4.56);

// 文字列(string)
// 0文字以上の文字の並びはすべて文字列
// シングルクォート('')またはダブルクォート("")で全体を囲って記述
// --python-- str
// --python-- print("hello") print('hello')
const variableString = "I am a string";
console.log(typeof variableString);
console.log("hello");
console.log(typeof "hello");

// ブーリアン(boolean)
// 真偽値のこと　true or false
// --python-- bool
// --python-- print(True) print(False)
const variableBoolean = true;
console.log(typeof variableBoolean);
console.log(false);
console.log(typeof false);

// undefined
// 未知数、未定義
const variableUndefined = undefined;
console.log(typeof variableUndefined);
